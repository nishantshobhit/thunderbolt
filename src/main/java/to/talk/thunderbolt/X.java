package to.talk.thunderbolt;

public class X {

    String verifiedMobile;
    String countryCode;
    String mobile;
    String version;
    String smsDisabled;

    public void setVerifiedMobile(String verifiedMobile) {
        this.verifiedMobile = verifiedMobile;
    }

    public void setCountryCode(String countryCode) {
        this.countryCode = countryCode;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public void setSmsDisabled(String smsDisabled) {
        this.smsDisabled = smsDisabled;
    }
}
